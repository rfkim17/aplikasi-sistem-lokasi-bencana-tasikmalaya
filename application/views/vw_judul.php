   <center> <h4> Pengertian judul bisa dengan gamblang kita temui di dalam Kamus Besar Bahasa Indonesia (KBBI). Judul ini merupakan bagian tak terpisahkan dari suatu karya. Meski keberadaannya lazim, namun mungkin sebagian dari kita tidak mengetahui definisinya.

Pembahasan
Di dalam KBBI, kata judul digolongkan sebagai kata benda atau nomina. Adapun artinya sebagai berikut:
</body>
<body>
● Nama yang digunakan untuk buku atau pun bab di dalam sebuah buku yang bisa menyiratkan secara ringkas isi pun maksud dari buku atau bab tersebut.
</body>
<marquee><body>
● Judul adalah kepala karangan. Karangan dalam hal ini bisa saja cerita, drama dan lain sebagainya.
</body></marquee>
<body>
● KBBI menyebut judul sebagai tajuk. Tajuk sendiri bermakna kepala tulisan berupa kalimat pendek.

Kesimpulan
Judul merupakan kepala suatu karya dalam bentuk kalimat pendek (umumnya frasa) yang mewakili karya tersebut secara keseluruhan. Karya ini bisa teks, audio (lagu) atau pun video (film dan sebagainya). </h4></center>
